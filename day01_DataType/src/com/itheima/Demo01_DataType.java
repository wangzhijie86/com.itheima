package com.itheima;
/*
	常量：
		在程序运行过程中，其值不可以发生改变的量。

	常量分类：
		字符串常量：	用双引号括起来的内容。"HelloWorld","黑马程序员"
		整数常量：		不带小数的数字。666,-88
		小数常量：		带小数的数字。13.14,-5.21
		字符常量：		用单引号括起来的内容。'A','0','我'
		布尔常量：		布尔值，表示真假。true,false
		空常量：		一个特殊的值，空值。null
*/
public class Demo01_DataType {
    public static void main(String[] args) {

        //整型integer，Int
        //byte 、short 、int 、long
        //定义byte类型的变量
        byte b = 10;
        System.out.println(b);

        //定义short类型的变量
        short s = 100;
        System.out.println(s);

        //定义int类型的变量
        int i = 10000;
        System.out.println(i);

        //定义double类型的变量
        double d = 13.14;
        System.out.println(d);

        //定义char类型的变量
        char c = 'a';
        System.out.println(c);

        //定义boolean类型的变量
        //boolean b = true;
        //System.out.println(b);
        boolean bb = true;
        System.out.println(bb);
        System.out.println("--------");

        //定义long类型的变量
        long l = 10000000000L;
        System.out.println(l);
        System.out.println("--------");

        //定义float类型的变量
        float f = 13.14F;
        System.out.println(f);




























    }
}
